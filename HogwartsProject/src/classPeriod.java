
public class classPeriod
	{
		private int Grade, Period;
		public classPeriod(int g, int p)
		{
			Grade=g;
			Period=p;
		}
		public int getGrade()
			{
				return Grade;
			}
		public void setGrade(int grade)
			{
				Grade = grade;
			}
		public int getPeriod()
			{
				return Period;
			}
		public void setPeriod(int period)
			{
				Period = period;
			}
		
	}
